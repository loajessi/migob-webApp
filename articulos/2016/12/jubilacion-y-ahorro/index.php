        <h1 class="tituloArticulo">Jubilación y Ahorro</h1>
            <p><img style="display: block; margin-left: auto; margin-right: auto;" src="http://i.giphy.com/3o7TKrcxO38x6GFurC.gif" alt="Ahorro y jubilación." /></p>
            <h1 style="color: #5e9ca0;"><span style="color: #003366;">Las 10 maneras para prepararse para su jubilaci&oacute;n&nbsp;</span></h1>
            <p>El Departamento de Trabajo (DOL por sus siglas en ingl&eacute;s, Department of Labor) ha preparado la siguiente lista de 10 recomendaciones para prepararse para la jubilaci&oacute;n.&nbsp;</p>
            <p><strong>1. Determine cu&aacute;les seran sus necesidades cuando se retire.&nbsp;</strong></p>
            <p>Retirarse es costoso. Los expertos estiman que,&nbsp;para poder mantener su nivel de vida cuando se retire, una persona necesitar&aacute; recibir el equivalente de alrededor del 70% de sus ingresos cuando trabajaba (casi el 90%, o m&aacute;s, en el caso de las personas con ingresos menores).&nbsp;Es muy importante que usted determine cu&aacute;l ser&aacute; su situaci&oacute;n financiera futura.</p>
            <p><strong>2. Inf&oacute;rmese sobre sus prestaciones del Seguro Social.</strong></p>
            <p>El Seguro Social paga al retirado promedio cerca del 40% de sus ingresos antes del retiro. Para obtener sin costo alguno la declaraci&oacute;n de sus ingresos personales y prestaciones estimadas (Personal Earnings and Benefit Estimate Statement, PEBES), llame a la Administraci&oacute;n del Seguro Social al n&uacute;mero 1-800-772-1213.</p>
            <p><strong>3. Inf&oacute;rmese acerca del plan de retiro o de participaci&oacute;n en las utilidades que provee su empleador.</strong></p>
            <p>Si su empleador ofrece un plan, pida que le proporcione el valor estimado de sus prestaciones. La mayor parte de las empresas le proporcionar&aacute; un resumen de sus prestaciones individuales, si usted lo solicita. Antes de cambiar de empleo, pregunte qu&eacute; suceder&aacute; con su pensi&oacute;n. Averig&uuml;e si recibir&aacute; las prestaciones del plan de su empleo anterior. Y, determine tambi&eacute;n si tendr&aacute; derecho a recibir las prestaciones del plan de su c&oacute;nyuge. Para obtener un folleto gratuito sobre pensiones privadas, llame a la l&iacute;nea directa de publicaciones de la Employee Benefits Security Administration al 1-800-998-7542.</p>
            <p><strong>4. Contribuya a un plan de ahorro que no est&eacute; sujeto al pago de impuestos.</strong></p>
            <p>Si su empleador ofrece un plan de ahorros que est&eacute; libre del pago de impuestos, tal como un 401(k), participe y contribuya lo m&aacute;s que pueda. Sus impuestos sobre estos ahorros ser&aacute;n menores, su compa&ntilde;&iacute;a podr&iacute;a tambi&eacute;n aportar a su cuenta, y las deducciones autom&aacute;ticas de su sueldo facilitar&aacute;n el tr&aacute;mite.&nbsp;</p>
            <p><strong>5. P&iacute;dale a su empleados que establezca su plan.</strong></p>
            <p>Si su empleador no ofrece un plan de retiro, prop&oacute;ngale que instituya uno. Muchas compa&ntilde;&iacute;as peque&ntilde;as pueden tambi&eacute;n establecer planes simplificados. Llame al Servicio de Rentas Internas (Internal Revenue Service, IRS) al 1-800-829-3676 para obtener informaci&oacute;n gratuita sobre estos planes.</p>
            <p><strong>6. Deposite sus ahorros en una cuenta de retiro individual (Individual Retirement Account, IRA)</strong></p>
            <p>Podr&aacute; invertir $2.000 al a&ntilde;o en una cuenta IRA sin pagar impuestos sobre los intereses devengados sino hasta cuando se retire. Si no tiene un plan de retiro (o si participa en un plan, pero lo que recibe es inferior a una cantidad espec&iacute;fica), puede tambi&eacute;n deducir de sus impuestos las contribuciones a su cuenta IRA. Obtenga un ejemplar gratuito de la publicaci&oacute;n 590 del IRS, en la cual encontrar&aacute; informaci&oacute;n relativa a las cuentas IRA.</p>
            <p>La ventaja de comenzar temprano.</p>
            <p>Comience hoy mismo! En el cuadro que figura a continuaci&oacute;n encontrar&aacute; la suma que acumular&iacute;a despu&eacute;s de 10, 20 y 30 a&ntilde;os, si usted comenzara a ahorrar $2.000 al a&ntilde;o y su dinero devengara un 4% de inter&eacute;s anual (por encima de la inflaci&oacute;n).</p>
            <p>A&ntilde;os</p>
            <p>0 - $2,000</p>
            <p>10 - $24,012</p>
            <p>20 - $59,556</p>
            <p>30 - $112,170</p>
            <p>&nbsp;</p>
            <p><strong>7. No gastes sus ahorros.</strong></p>
            <p>No use sus ahorros para su retiro. Perder&aacute; tanto capital como intereses y no se beneficiar&aacute; de las ventajas impositivas de este tipo de ahorro. Si cambia de empleo, transfiera sus ahorros directamente a una cuenta IRA o al plan de retiro de su nuevo empleador.&nbsp;</p>
            <p><strong>8. Comience hoy mismo, establezca sus objetivos y tome las medidas necesarias para lograrlos.</strong></p>
            <p>Comience lo antes posible. Mientras m&aacute;s temprano empiece a ahorrar, m&aacute;s tiempo tendr&aacute; para que sus ahorros crezcan. Convierta el ahorro en su prioridad m&aacute;s importante. Formule un plan, s&iacute;galo y establezca las metas para usted y su familia. Recuerde que nunca es demasiado tarde para ahorrar. Comience a ahorrar hoy mismo, sin importar la edad que tenga.</p>
            <p><strong>9. Considere los principios b&aacute;sicos de la inveresi&oacute;n.</strong></p>
            <p>La manera en que se ahorra es tan importante como la suma ahorrada. La inflaci&oacute;n y los tipos de inversi&oacute;n que usted haga son factores de suma importancia, y de ellos depender&aacute; el total de sus ahorros al retirarse. Inf&oacute;rmese sobre la manera en que est&aacute; invertido su plan de retiro o de ahorros.</p>
            <p><strong>10. Haga sus preguntas.</strong></p>
            <p>Las sugerencias anteriores le servir&aacute;n para comenzar a prepararse financieramente para su retiro, pero deber&aacute; obtener m&aacute;s informaci&oacute;n. Hable con su empleador, banco, cooperativa de cr&eacute;dito, o un asesor financiero. Haga sus preguntas y aseg&uacute;rese de haber entendido las respuestas. Obtenga consejos pr&aacute;cticos y act&uacute;e hoy mismo.</p>
            <p>La seguridad econ&oacute;mica no se logra por arte de magia. Se requiere planificaci&oacute;n, disciplina y, por supuesto, dinero.</p>
            <p>Puede leer el art&iacute;culo original publicado por el Departamento de Trabajo en <a href="https://www.dol.gov/ebsa/publications/top10spn.htm">ESTE ENLACE</a> que contiene referencias a recursos que pueda consultar.&nbsp;</p>
            <p>Tambi&eacute;n el Departamento de Trabajo ha preparado <a href="https://www.dol.gov/sites/default/files/ebsa/about-ebsa/our-activities/resource-center/publications/savingsfitnesssp.pdf">ESTE MANUAL ILUSTADO</a> con recomendaciones para ahorrar y prepararse para el retiro.</p>
